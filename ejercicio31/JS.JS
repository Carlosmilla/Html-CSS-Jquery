function cambiarcolor(){
    var crojo = $("#clrojo").val();
    var cverde = $("#clverde").val();
    var cazul = $("#clazul").val();
    var colorrgb = "rgb(" + crojo + "," + cverde + "," + cazul + ")";

    $("#contcolor").css("background-color", colorrgb);
}

$("#clrojo").change(function(){
    $("#txtrojo").val($("#clrojo").val());
    cambiarcolor();
});

$("#clverde").change(function(){
    $("#txtverde").val($("#clverde").val());
    cambiarcolor();
});

$("#clazul").change(function(){
    $("#txtazul").val($("#clazul").val());
    cambiarcolor();
});
